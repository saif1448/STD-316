
# Break and Continue

# Break
# special condition if number is > 7, we will not iterate the loop
# If you add break statement, it won't run any line of code under the break statement inside the loop

for i in range(1, 11):
    if i > 7:
        break
    print(f"We are inside the loop {i}")
    print("asdasd")
    print("qweqwe")



print("This is outside the loop")