# slicing example

str1 = "This is a sample string"

print(str1[1:4]) # ending index is excluded --> it is giving substring in a range

print(str1[:11]) # from begining to index 10

print(str1[3:]) # from index 3 to end

print(str1[:]) # it is gonna slice from beg to end


print(str1[::3]) #:3 --> taking char after 2 char

