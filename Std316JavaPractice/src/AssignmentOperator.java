public class AssignmentOperator {

    public static void main(String[] args) {
        int x = 10;
        int y = 5;

        x = x + y;
        //shorthand assignment operator
        x += y; // x = x+ y
        x -= y; // x = x - y
        x *= y; // x = x * y
        x /= y; // x = x / y
    }
}
