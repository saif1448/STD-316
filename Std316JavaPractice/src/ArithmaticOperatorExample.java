public class ArithmaticOperatorExample {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;

        // Addition
        int result = x + y;
        System.out.println(result);


        //Subtraction
        result = x - y;
        System.out.println(result);

        //Multiplication
        result = x * y;
        System.out.println(result);

        //Division
        result = x / y;
        System.out.println(result);

        //Modulus
        result = x % y;
        System.out.println(result);
        x = 12;
        result = x % y;
        System.out.println(result);

    }
}
